# %= Modulus and Assign Operator in python #
invoice_value = 15
invoice_value %= 2
print(invoice_value)

""" modulus %= operators only works with integers but doesnt work with 
strings, list, sets or tuples """ 

a = 10.2
a %= 2.1
print(a)


